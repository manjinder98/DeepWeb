            <div class="row">
				<div class="col-lg-8 col-lg-offset-2 text-center">
					<h2 class="section-heading"> Security in Tor </h2>
					<hr class="primary">
					  <div class="thumbnail" style="border:none; background:white;">
						  <div>  
								<p class="text-dark">
								Tor does not completely guarantee anonymity because there are various factors that compromise security: 
							</p>
						  </div>
						  <div class="list-group">
							  <a class="list-group-item list-group-item-action">
								<p>The <strong>NSA (National Security Agency)</strong> has the skills to enter the user's browser and once it does,
                                                                 it gains access to everything else. Thus, <em>"man in the middle"</em> attacks are still possible with the help of Internet service providers.
                                                                 Therefore Tor does not guarantee anonymity, but a certain security that the user can use.</p>
							  </a>
							  <a class="list-group-item list-group-item-action">
								  <div class="d-flex w-100 justify-content-between">
									  <h4 class="mb-1">The exit nodes can be sniffed</h4>
								   </div>  
									   <p>If you're accessing an encrypted site <strong>(HTTPS)</strong> like your Gmail account, you have a security breach, even if the exit node can see that you're connecting to Gmail.<br>
                                                                                If you log in to an unencrypted website, the exit node can potentially monitor Internet activity, keep track of web pages you visit, searches you've made, and sent messages, and this will compromise your security a lot.<br>
                                                                                Therefore Tor advised against browsing on sites of type http, ie unencrypted, or using javascript or various plugins, because all this allows the exit node to have more information about what you are visiting, 
                                                                                going to identify the IP address as well of the user's device.</p>
							   </a>
							   <a class="list-group-item list-group-item-action">
								   <p>Another compromising factor is that governments are likely to manage some exit nodes and also control the traffic that passes through them, using this to investigate criminals in repressive countries or to punish political activists.</p>
							   </a>
						  </div>	
						  <div>
							  <h2 class="section-heading"> Solution </h2>
							  <hr class="primary"> 	
							  <p style="text-align: left;">
								So be sure to use anonymity, you must use <strong>HTTPS</strong> whenever possible to protect yourself from <em>man-in-the-middle</em> attacks.
                                                                Tor is not a magic solution that guarantees anonymity. Obtained anonymity encrypted traffic goes through the Tor network, but this traffic must go out somewhere, and this is a very serious problem for Tor users and exit node operators, who have not yet arrived to a solution.
							  </p>
						  </div>
